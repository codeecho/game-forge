package uk.co.codeecho.gdx.forge.tmx;

public class TMXMapProperties {

    private float width;
    private float height;

    public TMXMapProperties(float width, float height) {
        this.width = width;
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }

}
